export function isVerboseEnvelope() {
  return process.env.BREW_MVP_VERBOSE_ENVELOPE === "1";
}

function loadProfileTextFromDatasetPath(datasetPath) {
  if (!datasetPath) return { headline: "", about: "", rawDataset: null };
  try {
    const raw = readFileSync(String(datasetPath), "utf8");
    const obj = JSON.parse(raw);
    const profile = obj?.profile || {};
    return {
      headline: String(profile.headline || "").trim(),
      about: String(profile.about || "").trim(),
      rawDataset: obj,
    };
  } catch {
    return { headline: "", about: "", rawDataset: null };
  }
}

/** @param {Record<string, unknown>} envelope */
export function applySlimEnvelope(envelope) {
  if (isVerboseEnvelope() || !envelope) return envelope;
  const fromDataset = loadProfileTextFromDatasetPath(envelope?.run?.datasetPath);
  const out = buildSlimResults(envelope.results, fromDataset.rawDataset);
  if (out?.sub_analyses?.headline && !out.sub_analyses.headline.headline_text) {
    out.sub_analyses.headline.headline_text = fromDataset.headline || "";
  }
  if (out?.sub_analyses?.about && !out.sub_analyses.about.about_text) {
    out.sub_analyses.about.about_text = fromDataset.about || "";
  }
  if (out?.composite_classification) {
    out.composite_classification.math_based_evaluation = computeMathBasedEvaluation({
      out,
      dataset: fromDataset.rawDataset,
    });
  }
  out.behavior_analyzer = runBehaviorAnalyzerFlow(fromDataset.rawDataset);
  out.content_analyzer = runContentAnalyzerFlow(
    fromDataset.rawDataset,
    out,
    out.behavior_analyzer
  );
  out.comparative_intelligence = runComparativeAnalyzerFlow(
    fromDataset.rawDataset,
    out,
    out.behavior_analyzer,
    out.content_analyzer
  );
  out.reporting_recommendations = runReportingRecommendationsFlow(
    fromDataset.rawDataset,
    out,
    out.behavior_analyzer,
    out.content_analyzer,
    out.comparative_intelligence
  );
  out.five_module_system = buildFiveModuleSystem({
    out,
    dataset: fromDataset.rawDataset,
  });
  out.module_interactions = buildModuleInteractions({ out });
  out.output_specifications = buildOutputSpecifications({ out });
  applyProfileTuningToSlimOutput(out, fromDataset.rawDataset?.scoringTuning?.profile);

  const pr = fromDataset.rawDataset?.profile;
  if (pr && typeof pr === "object") {
    out.profile_inputs_echo = {
      experience_items: Array.isArray(pr.experienceItems) ? pr.experienceItems.slice(0, 8) : [],
      certifications: Array.isArray(pr.certifications) ? pr.certifications : [],
      education: Array.isArray(pr.education) ? pr.education : [],
      featured: Array.isArray(pr.featured) ? pr.featured : [],
      recommendation_blurbs: Array.isArray(pr.recommendations)
        ? pr.recommendations.map((r) => {
            if (typeof r === "object" && r !== null) {
              return `${r.name || ""}, ${r.role || ""}: "${r.message || ""}"`.trim();
            }
            return String(r);
          })
        : [],
      skills_ordered: Array.isArray(pr.skills) ? pr.skills.map(String).slice(0, 36) : [],
      note: "Fields copied from your dataset for UI traceability. Layer 1 verbalizes these for the LLM; sub_analyses scores come from the analysis JSON unless a row was padded from the dataset (see issues_found on that role).",
    };
  }
  out.run = envelope?.run || null;
  return out;
}
