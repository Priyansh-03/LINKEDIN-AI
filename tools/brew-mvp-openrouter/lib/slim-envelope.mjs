function trunc(s, max) {
  const t = String(s || "").trim();
  if (t.length <= max) return t;
  return t.slice(0, max - 1) + "…";
}

function firstSentence(s) {
  const t = String(s || "").trim();
  const m = t.match(/^.{1,400}?[.!?](\s|$)/);
  if (m) return m[0].trim();
  return trunc(t, 320);
}

function pickText(obj, keys, fallback = "") {
  if (!obj || typeof obj !== "object") return fallback;
  for (const k of keys) {
    const v = obj[k];
    if (typeof v === "string" && v.trim()) return v.trim();
  }
  return fallback;
}

function pickNum(obj, keys) {
  if (!obj || typeof obj !== "object") return null;
  for (const k of keys) {
    const raw = obj[k];
    if (typeof raw === "number" && Number.isFinite(raw)) return raw;
    if (typeof raw === "string" && raw.trim() !== "") {
      const n = Number(String(raw).trim().replace(/,/g, ""));
      if (Number.isFinite(n)) return n;
    }
  }
  return null;
}

function normalize100(v) {
  if (typeof v !== "number" || !Number.isFinite(v)) return null;
  if (v >= 0 && v <= 10) return Math.round(v * 10);
  if (v >= 0 && v <= 100) return Math.round(v);
  return null;
}

function score10(section, keys, fallback = 7) {
  const n = normalize100(pickNum(section, keys));
  if (n == null) return fallback;
  return Math.max(1, Math.min(10, Math.round(n / 10)));
}

/**
 * Pass A nests rubric fields under `scores`. `score10(section, …)` used to read
 * only the section root, so every axis fell back to 7. Merge scores onto the
 * lookup object so slim output reflects the model’s 1–10 rubric.
 * @param {Record<string, unknown>|null|undefined} section
 * @param {string[]} keys
 * @param {number} [fallback]
 */
function scoreBlock(section, keys, fallback = 7) {
  if (!section || typeof section !== "object") return fallback;
  const scores = section.scores;
  const merged =
    scores && typeof scores === "object" && !Array.isArray(scores)
      ? /** @type {Record<string, unknown>} */ ({ ...section, ...scores })
      : section;
  return score10(merged, keys, fallback);
}

function deriveSection100(value, keys = [], fallbackValue = 70) {
  const direct = normalize100(pickNum(value, keys));
  if (direct != null) return direct;

  // Fallback 1: if 'scores' object exists, average the values
  const innerScores = value?.scores;
  if (innerScores && typeof innerScores === "object") {
    const sValues = Object.values(innerScores)
      .map((v) => normalize100(v))
      .filter((v) => v != null);
    if (sValues.length) return Math.round(sValues.reduce((a, b) => a + b, 0) / sValues.length);
  }

  // Fallback 2: Array averaging (for experience)
  if (Array.isArray(value)) {
    const nums = value
      .map((x) => normalize100(pickNum(x, Object.keys(x || {}))))
      .filter((x) => x != null);
    if (nums.length) return Math.round(nums.reduce((a, b) => a + b, 0) / nums.length);
  }

  return fallbackValue;
}

function mapIssues(items, max = 3) {
  const list = Array.isArray(items) ? items : [];
  const out = [];
  const seen = new Set();
  for (const x of list) {
    const issue = trunc(x?.issue, 240);
    if (!issue) continue;
    const key = issue.toLowerCase().replace(/[^a-z0-9]+/g, " ").trim();
    if (!key || seen.has(key)) continue;
    seen.add(key);
    /** @type {Record<string, unknown>} */
    const row = {
    severity: String(x?.severity || "medium").toLowerCase(),
      issue,
      confidence: toConfidenceEmoji(x?.confidence),
      claim_type: x?.claim_type || toClaimTypeFromConfidence(x?.confidence),
    fix: trunc(x?.fix, 240),
    };
    if (Array.isArray(x?.keywords_to_add) && x.keywords_to_add.length) {
      row.keywords_to_add = x.keywords_to_add.map(String).filter(Boolean).slice(0, 8);
    }
    out.push(row);
    if (out.length >= max) break;
  }
  return out;
}

/**
 * Recruiter-searchable phrases from dataset (declared niche, headline, skills)
 * that are absent from current About text. PRD-aligned specificity (§7.3.2, Principle 2).
 * @param {Record<string, unknown>|null|undefined} rawDataset
 */
function keywordSuggestionsForAbout(rawDataset) {
  if (!rawDataset || typeof rawDataset !== "object") return [];
  const uc = /** @type {Record<string, unknown>} */ (rawDataset.userContext || {});
  const profile = /** @type {Record<string, unknown>} */ (rawDataset.profile || {});
  const aboutLower = String(profile.about || "").toLowerCase();
  const pool = [];

  const push = (s) => {
    const t = String(s || "").trim();
    if (t.length < 3 || t.length > 56) return;
    const k = t.toLowerCase();
    if (aboutLower.includes(k)) return;
    pool.push(t);
  };

  String(uc.declared_niche || "")
    .split(/[,|]+/)
    .forEach((x) => push(x));

  String(uc.target_audience || "")
    .split(/[,]+/)
    .forEach((x) => push(x));

  String(profile.headline || "")
    .split("|")
    .forEach((x) => push(x));

  const skills = Array.isArray(profile.skills) ? profile.skills : [];
  for (const sk of skills) push(sk);

  const uniq = [];
  const seen = new Set();
  for (const p of pool) {
    const key = p.toLowerCase();
    if (seen.has(key)) continue;
    seen.add(key);
    uniq.push(p);
    if (uniq.length >= 12) break;
  }
  return uniq.slice(0, 8);
}

/**
 * @param {unknown[]} issues
 * @param {Record<string, unknown>|null|undefined} rawDataset
 */
function enrichAboutIssuesWithKeywords(issues, rawDataset) {
  if (!rawDataset || !Array.isArray(issues) || !issues.length) return issues;
  const kws = keywordSuggestionsForAbout(rawDataset);
  if (!kws.length) return issues;
  return issues.map((row, i) => {
    if (!row || typeof row !== "object") return row;
    const r = /** @type {Record<string, unknown>} */ (row);
    if (Array.isArray(r.keywords_to_add) && r.keywords_to_add.length) return r;
    const slice = i === 0 ? kws.slice(0, 4) : kws.slice(4, 8);
    const effective = slice.filter((x) => typeof x === "string" && x.trim());
    if (!effective.length) return r;
    const extra = ` Weave in searchable terms: ${effective.join(", ")}.`;
    return {
      ...r,
      keywords_to_add: effective,
      fix: trunc(String(r.fix || "") + extra, 300),
    };
  });
}

