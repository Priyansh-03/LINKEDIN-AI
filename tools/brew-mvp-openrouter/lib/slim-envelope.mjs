/**
 * PRD §7.3.3: current role + two most recent past roles (≤3). When Pass A/B returns fewer
 * `sub_analyses.experience` rows than `profile.experienceItems`, pad from dataset titles
 * so the slim report matches user input.
 * @param {unknown[]} llmExp
 * @param {Record<string, unknown>|null|undefined} rawDataset
 */
function buildExperienceRolesForSlim(llmExp, rawDataset) {
  const items = Array.isArray(rawDataset?.profile?.experienceItems) ? rawDataset.profile.experienceItems : [];
  const llm = Array.isArray(llmExp) ? llmExp : [];
  const n = Math.min(3, Math.max(llm.length, items.length));
  if (n <= 0) return [];
  const roles = [];
  for (let idx = 0; idx < n; idx++) {
    const r = llm[idx] && typeof llm[idx] === "object" ? /** @type {Record<string, unknown>} */ (llm[idx]) : {};
    const ds = items[idx] && typeof items[idx] === "object" ? /** @type {Record<string, unknown>} */ (items[idx]) : {};
    const roleTitle = trunc(pickText(r, ["role", "title"], String(ds.title || `Role ${idx + 1}`)), 120);
    const company = trunc(pickText(r, ["company"], String(ds.company || "")), 100);
    const hasScores = r && typeof r.scores === "object" && r.scores != null;
    const scores = hasScores
      ? {
          scope_clarity: scoreBlock(r, ["scope_clarity", "scope_clarity_1_to_10"]),
          outcome_density: scoreBlock(r, ["outcome_density", "outcome_density_1_to_10"]),
          strategic_tactical_balance: scoreBlock(r, ["strategic_tactical_balance", "strategic_vs_tactical_balance"]),
          verb_strength: scoreBlock(r, ["verb_strength", "verb_strength_1_to_10"]),
          skill_tagging_coherence: scoreBlock(r, ["skill_tagging_coherence", "skill_tagging_coherence_1_to_10"]),
          recency_treatment: scoreBlock(r, ["recency_treatment", "recency_treatment_1_to_10"]),
        }
      : { ...DEFAULT_EXP_SCORES };

    let issuesFound = mapIssues(r?.top_issues, 3);
    if (!issuesFound.length && (ds.title || ds.company)) {
      issuesFound = [
        {
          severity: "medium",
          issue: "No separate LLM scorecard for this role row (filled from your dataset).",
          confidence: "🟡",
          claim_type: "practitioner_observed",
          fix: trunc(`Add 2–3 outcome bullets with metrics for ${roleTitle} @ ${company}.`, 240),
        },
      ];
    }

    roles.push({
      role: roleTitle,
      company,
      scores,
      issues_found: issuesFound,
    });
  }
  return roles;
}

/** @param {Record<string, unknown>|null|undefined} rawDataset */
function profileFromDataset(rawDataset) {
  if (!rawDataset || typeof rawDataset !== "object") return {};
  const profile = rawDataset.profile;
  return profile && typeof profile === "object" ? /** @type {Record<string, unknown>} */ (profile) : {};
}

function titleTokens(title) {
  return String(title || "")
    .toLowerCase()
    .split(/\W+/)
    .filter((w) => w.length > 4);
}

/**
 * Evidence-based headline issues when Pass A returns no `issues_found` — avoids static
 * "always the same" fallbacks; uses Experience + Skills + headline text (PRD holistic).
 * @param {Record<string, unknown>|null|undefined} rawDataset
 */
function heuristicHeadlineIssues(rawDataset, max) {
  const profile = profileFromDataset(rawDataset);
  const headline = String(profile.headline || "").trim();
  if (!headline) {
    return [
      {
        severity: "high",
        issue: "Headline is empty in the dataset.",
        confidence: "🟡",
        claim_type: "practitioner_observed",
        fix: "Add a searchable headline: role level, domain, and one scope or outcome hint consistent with your experience.",
      },
    ];
  }

  const lower = headline.toLowerCase();
  const seniorityRe =
    /\b(director|vp\b|vice[\s-]president|svp|evp|head\b|lead\b|principal\b|staff\b|chief|founder|co-founder|founding|senior|manager|architect|fellow|distinguished|partner|ceo|cto|cfo|cxo|gm\b|president|executive|leader\b)\b/i;
  const yearsHint = /\b(\d{1,2}\s*\+?\s*(yrs|years))\b/i.test(headline) || /\d{1,2}\+/.test(headline);

  const items = Array.isArray(profile.experienceItems) ? profile.experienceItems : [];
  let experienceEcho = false;
  for (const e of items.slice(0, 3)) {
    for (const tok of titleTokens(e?.title)) {
      if (lower.includes(tok)) {
        experienceEcho = true;
        break;
      }
    }
    if (experienceEcho) break;
    const co = String(e?.company || "")
      .toLowerCase()
      .replace(/[^a-z0-9]+/g, " ")
      .trim();
    if (co.length > 3 && lower.includes(co)) {
      experienceEcho = true;
      break;
    }
  }

  const skills = Array.isArray(profile.skills) ? profile.skills.map(String) : [];
  let skillEcho = false;
  for (const s of skills) {
    const sk = s.toLowerCase().trim();
    if (sk.length > 4 && lower.includes(sk)) {
      skillEcho = true;
      break;
    }
  }

  const hasPipe = headline.includes("|");
  const hasMetricOrDigit = /\d/.test(headline);
  const domainWords = /\b(ai|ml|llm|nlp|genai|data|cloud|security|saas|product|growth|engineering|software|research)\b/i.test(headline);
  const seniorityOk = seniorityRe.test(headline) || yearsHint;

  const strongPackaging =
    seniorityOk ||
    experienceEcho ||
    skillEcho ||
    (hasPipe && (domainWords || hasMetricOrDigit)) ||
    (headline.length >= 52 && (domainWords || hasMetricOrDigit || hasPipe));

  /** @type {Record<string, unknown>[]} */
  const out = [];
  if (strongPackaging) {
    if (headline.length < 30 && !hasPipe && !hasMetricOrDigit && items.length >= 1) {
      const t0 = String(items[0]?.title || "your current role").slice(0, 88);
      out.push({
        severity: "medium",
        issue: "Headline is very short compared with your experience depth — one more specificity clause could lift search match.",
        confidence: "🟡",
        claim_type: "practitioner_observed",
        fix: trunc(`Borrow one concrete scope or outcome phrase from "${t0}" or your top skills so scanners see niche + impact immediately.`, 240),
      });
    }
    return out.slice(0, max);
  }

  if (items.length >= 1 && (String(items[0]?.title || "").length > 2 || String(items[0]?.description || "").length > 40)) {
    const t0 = String(items[0]?.title || "Current role").slice(0, 88);
    out.push({
      severity: "medium",
      issue: "Headline undersells niche or seniority that already appears in your Experience section.",
      confidence: "🟡",
      claim_type: "practitioner_observed",
      fix: trunc(`Echo one strong signal from your roles (e.g. ${t0}) and a domain keyword from Skills so the headline matches the rest of the profile.`, 240),
    });
    return out.slice(0, max);
  }

  out.push({
    severity: "high",
    issue: "Headline does not yet combine niche, seniority, and one concrete proof or scope hint.",
    confidence: "🟡",
    claim_type: "practitioner_observed",
    fix: "Add role level, domain keyword, and one measurable or scope outcome aligned with your target searches.",
  });
  return out.slice(0, max);
}

/**
 * Evidence-based About issues when Pass A returns no `issues_found`.
 * @param {Record<string, unknown>|null|undefined} rawDataset
 */
function heuristicAboutIssues(rawDataset, max) {
  const profile = profileFromDataset(rawDataset);
  const about = String(profile.about || "").trim();
  if (!about) {
    return [
      {
        severity: "high",
        issue: "About section is empty in the dataset.",
        confidence: "🟡",
        claim_type: "practitioner_observed",
        fix: "Add a short hook, proof from your roles, and a clear reader CTA aligned with your audience.",
      },
    ];
  }

  const n = about.length;
  if (n < 140) return [];

  const firstLen = Math.max(140, Math.floor(n * 0.42));
  const first = about.slice(0, firstLen);
  const proofRe =
    /\d+\s*%|\d+\s*x\b|\$\s*\d|(\d{1,3},\d{3})|\d+\s*(k\+|m\+|million|thousand|users|customers|teams|people)\b|\b(reduced|increased|grew|scaled|shipped|launched|saved|cut|improved|drove|delivered)\b/i;
  const hasProofInHook = proofRe.test(first);
  const expDense = Array.isArray(profile.experienceItems) && profile.experienceItems.some((e) => String(e?.description || "").length > 80);

  const ctaRe =
    /\b(dm|message me|reach out|let'?s connect|ping me|contact|@\w|hire me|open to|collaborat|book (a|my)|email me|calendar)\b/i;
  const hasCta = ctaRe.test(about);

  /** @type {Record<string, unknown>[]} */
  const out = [];
  if (!hasProofInHook && n >= 180) {
    out.push({
      severity: expDense ? "medium" : "high",
      issue: "About opens with few concrete proof points while the profile has more story to tell.",
      confidence: "🟡",
      claim_type: "practitioner_observed",
      fix: expDense
        ? "Move 1–2 measurable outcomes from your experience bullets into the first paragraph (numbers, %, scope, or time windows)."
        : "Add 1–2 quantified outcomes or scope facts in the opening lines so the hook matches your seniority.",
    });
  }
  if (!hasCta && n >= 220) {
    out.push({
      severity: "medium",
      issue: "About does not state an explicit reader CTA (who should reach out and for what).",
      confidence: "🟡",
      claim_type: "practitioner_observed",
      fix: "Close with one specific line: hiring, advising, building together, or the roles you want inbound for.",
    });
  }
  return out.slice(0, max);
}

/**
 * @param {string} sectionName
 * @param {unknown} sectionSpecific
 * @param {unknown} globalIssues
 * @param {Record<string, unknown>|null|undefined} rawDataset
 * @param {number} [max]
 */
function deriveSectionIssues(sectionName, sectionSpecific, globalIssues, rawDataset = null, max = 3) {
  const local = mapIssues(sectionSpecific, max);
  if (local.length) return local;

  if (sectionName === "headline" && rawDataset) {
    const h = heuristicHeadlineIssues(rawDataset, max);
    if (h.length) return h;
  } else if (sectionName === "about" && rawDataset) {
    const a = heuristicAboutIssues(rawDataset, max);
    if (a.length) return a;
  } else if (sectionName !== "headline" && sectionName !== "about") {
    return mapIssues(globalIssues, max);
  }

  const globalMapped = mapIssues(globalIssues, Math.max(max, 10));
  if (globalMapped.length) {
    const re =
      sectionName === "headline"
        ? /headline|search|keyword|hook|positioning|title line|character|discover/i
        : /about|summary|cta|275|first paragraph|hook|story|narrative/i;
    const filtered = globalMapped.filter((x) => re.test(String(x.issue || "") + " " + String(x.fix || "")));
    if (filtered.length) return filtered.slice(0, max);
  }

  return [];
}

function buildHeadlineRewriteOptions(rawOptions, headlineText, max = 4) {
  const cleanedHeadline = trunc(headlineText, 200);
  const normalized = new Set();
  const out = [];
  function pushUnique(value) {
    const txt = trunc(value, 220);
    if (!txt) return;
    const key = txt.toLowerCase().replace(/[^a-z0-9]+/g, " ").trim();
    if (!key || normalized.has(key)) return;
    normalized.add(key);
    out.push(txt);
  }

  if (Array.isArray(rawOptions)) {
    rawOptions.forEach((x) => pushUnique(String(x || "")));
  }
  pushUnique(cleanedHeadline);

  const roleChunk = cleanedHeadline.split("|")[0]?.trim() || "AI/ML professional";
  const nicheChunk = cleanedHeadline.includes("|")
    ? cleanedHeadline.split("|").slice(1).join("|").trim()
    : "LLMs, NLP, Cloud";
  const fallbacks = [
    `${roleChunk} | Building production-grade AI systems | ${nicheChunk}`,
    `${roleChunk} | Applied GenAI and NLP for business outcomes`,
    `${roleChunk} | Turning AI prototypes into scalable products`,
    `${roleChunk} | LLM applications, chatbot systems, and cloud deployment`,
  ];
  fallbacks.forEach(pushUnique);
  return out.slice(0, max);
}

/**
 * Four distinct About alternatives (same contract as headline.rewrite_options).
 * @param {unknown} rawOptions
 * @param {string} aboutText
 * @param {Record<string, unknown>|null|undefined} rawDataset
 * @param {number} [max]
 */
function buildAboutRewriteOptions(rawOptions, aboutText, rawDataset, max = 4) {
  const cleaned = trunc(String(aboutText || "").trim(), 400);
  const normalized = new Set();
  const out = [];
  function pushUnique(value) {
    const txt = trunc(String(value || "").trim(), 380);
    if (txt.length < 28) return;
    const key = txt.toLowerCase().replace(/[^a-z0-9]+/g, " ").trim();
    if (!key || normalized.has(key)) return;
    normalized.add(key);
    out.push(txt);
  }

  if (Array.isArray(rawOptions)) {
    rawOptions.forEach((x) => pushUnique(x));
  }
  pushUnique(cleaned);

  const uc =
    rawDataset && typeof rawDataset === "object" && rawDataset.userContext && typeof rawDataset.userContext === "object"
      ? /** @type {Record<string, unknown>} */ (rawDataset.userContext)
      : {};
  const nicheRaw = String(uc.declared_niche || uc.target_audience || "your niche").trim();
  const niche = trunc(nicheRaw.split(/[,|]/)[0]?.trim() || nicheRaw, 72) || "your niche";
  const hook = firstSentence(cleaned) || trunc(cleaned, 180);
  const tail = trunc(cleaned.slice(Math.min(cleaned.length, 120)), 220);

  const fallbacks = [
    `${hook}\n\nWho I help: teams focused on ${niche}. Proof: add 2–3 metrics from your roles. CTA: DM to discuss fit.`,
    `[Outcome-first] If you need ${niche} delivered reliably, here is how I work — then your story:\n${tail}`,
    `[Structure] (1) Positioning (2) Proof (3) How I work (4) Contact — tightened from your draft:\n${trunc(cleaned, 260)}`,
    `[Sharper hook] Same facts, less filler, stronger verbs — open with the problem you solve for ${niche}:\n${trunc(cleaned, 300)}`,
  ];
  fallbacks.forEach((x) => pushUnique(x));
  return out.slice(0, max);
}

function confidenceLabelFromEmoji(emoji) {
  if (emoji === "🟢") return "HIGH CONFIDENCE";
  if (emoji === "🟡") return "MEDIUM CONFIDENCE";
  return "SPECULATIVE";
}

function confidenceRationalePoints(claimType) {
  if (claimType === "paper_verified") {
    return [
      "🟢 Directly grounded in arXiv 2501.16450 / LinkedIn Engineering mechanics.",
      "🟢 Uses paper-aligned ranking or verbalization behavior.",
    ];
  }
  if (claimType === "practitioner_observed") {
    return [
      "🟡 Observed across multiple practitioner datasets/sources.",
      "🟡 Directionally inferred from paper-verified mechanics.",
    ];
  }
  return [
    "🔴 Explicitly marked as speculative due to limited verification.",
    "🔴 Treat as low-priority experiment, not core guidance.",
  ];
}

function buildIssueDisplayCard(issue, index) {
  const confidence = toConfidenceEmoji(issue?.confidence);
  const claimType = issue?.claim_type || toClaimTypeFromConfidence(issue?.confidence);
  let fixText = String(issue?.fix || "");
  if (Array.isArray(issue?.keywords_to_add) && issue.keywords_to_add.length) {
    fixText += `\nTerms to weave in: ${issue.keywords_to_add.join(", ")}`;
  }
  return {
    card_title: `ISSUE #${index + 1} [${confidence} ${confidenceLabelFromEmoji(confidence)}]`,
    issue: trunc(issue?.issue, 260),
    why_this_matters: trunc(
      issue?.issue || "This issue reduces profile-content-behavior coherence and weakens ranking signal quality.",
      360
    ),
    recommended_fix: trunc(fixText, 320),
    confidence: confidence,
    claim_type: claimType,
    confidence_rationale: {
      summary:
        claimType === "paper_verified"
          ? "Grounded in published 360Brew/LinkedIn mechanics."
          : claimType === "practitioner_observed"
            ? "Backed by repeated practitioner observation and directional inference."
            : "Marked speculative for transparency; use cautiously.",
      evidence_points: confidenceRationalePoints(claimType),
    },
  };
}

function clamp(min, v, max) {
  return Math.max(min, Math.min(max, v));
}

function avg(nums, fallback = 0) {
  const list = Array.isArray(nums) ? nums.filter((n) => typeof n === "number" && Number.isFinite(n)) : [];
  if (!list.length) return fallback;
  return Math.round(list.reduce((a, b) => a + b, 0) / list.length);
}

function toTier(score) {
  const s = clamp(0, Math.round(Number(score) || 0), 100);
  if (s >= 90) return "AUTHORITY_FIGURE";
  if (s >= 75) return "NICHE_SPECIALIST";
  if (s >= 50) return "GENERIC_PROFESSIONAL";
  return "CONFUSED_SIGNAL";
}

function computeBehaviorSignalScore(dataset) {
  const b = dataset?.behaviorSignals || {};
  const content = b?.contentEngagement || {};
  const network = b?.networkActions || {};
  const produced = b?.contentProduced || {};
  const searches = Array.isArray(b?.recentSearches) ? b.recentSearches.length : 0;

  const engagementRaw =
    Number(content.reactionsGiven || 0) * 1.0 +
    Number(content.commentsWritten || 0) * 2.0 +
    Number(content.reposts || 0) * 3.0 +
    Number(content.saves || 0) * 2.0;
  const networkRaw =
    Number(network.connectionRequestsSent || 0) * 1.0 +
    Number(network.connectionRequestsAccepted || 0) * 2.0 +
    Number(network.messagesSent || 0) * 1.0;
  const productionRaw =
    Number(produced.postsPublished || 0) * 20 +
    clamp(0, Number(produced.videoMinutesWatchedLearning || 0), 120) * 0.3;
  const searchRaw = searches * 20;

  const engagement = clamp(0, Math.round(engagementRaw), 100);
  const networkStrength = clamp(0, Math.round(networkRaw), 100);
  const production = clamp(0, Math.round(productionRaw), 100);
  const intent = clamp(0, Math.round(searchRaw), 100);
  const score = Math.round(engagement * 0.35 + networkStrength * 0.25 + production * 0.25 + intent * 0.15);

  return {
    score: clamp(0, score, 100),
    components: { engagement, network_strength: networkStrength, production, intent },
  };
}

function computeTaskInstructionQuality(dataset) {
  const t = String(dataset?.taskInstruction || "").trim();
  if (!t) return { score: 60, multiplier: 0.9 };
  const len = t.length;
  const score = clamp(60, Math.round(60 + Math.min(40, len / 4)), 100);
  const multiplier = len >= 80 ? 1.03 : 1.0;
  return { score, multiplier };
}

function computeMathBasedEvaluation({ out, dataset }) {
  const sa = out?.sub_analyses || {};
  const profileSignal = avg(
    [
      sa?.headline?.overall_headline_score,
      sa?.about?.overall_about_score,
      sa?.experience?.overall_experience_score,
      sa?.skills?.overall_skills_score,
      sa?.recommendations?.overall_recommendations_score,
      sa?.featured?.overall_featured_score,
    ],
    out?.composite_classification?.score ?? 70
  );
  const behavior = computeBehaviorSignalScore(dataset);
  const task = computeTaskInstructionQuality(dataset);
  const blendedRaw = 0.3 * profileSignal + 0.7 * behavior.score;
  const mathScore = clamp(0, Math.round(blendedRaw * task.multiplier), 100);
  const mathTier = toTier(mathScore);
  return {
    formula: "math_score = clamp(0,100, round((0.3*profile_signal_score + 0.7*behavior_signal_score) * task_instruction_multiplier))",
    weights: { profile_signal: 0.3, behavior_signal: 0.7 },
    profile_signal_score: profileSignal,
    behavior_signal_score: behavior.score,
    behavior_components: behavior.components,
    task_instruction_quality: task.score,
    task_instruction_multiplier: task.multiplier,
    math_score: mathScore,
    math_tier: mathTier,
  };
}

